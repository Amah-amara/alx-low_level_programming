#include "3-calc.h"

/**
 * op_add - calculates the sum of two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculates subtraction of two integers
 * @a: first integer
 * @b: second integer
 * Return: subtraction of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates product of two integers
 * @a: param1
 * @b: param2
 * Return: product of two integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculates division of two integers
 * @a: param1
 * @b: param2
 * Return: division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates the remainder of division of two integers
 * @a: param1
 * @b: param2
 * Return: remainder of division of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
